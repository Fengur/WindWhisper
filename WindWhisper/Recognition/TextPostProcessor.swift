        "感謝收看", "感谢收看",
        "下集再見", "下集再见",
        "貝爾", "Amara.org",
    ]

    private static func filterHallucinations(_ text: String) -> String {
        for pattern in hallucinations {
            if text.contains(pattern) {
                return ""
            }
        }
        if text.hasPrefix("(") && text.hasSuffix(")") && text.count < 20 {
            return ""
        }
        if text.hasPrefix("[") && text.hasSuffix("]") && text.count < 20 {
            return ""
        }
        return text
    }

