import Foundation

enum TextPostProcessor {

    static func process(_ text: String) -> String {
        var result = text
        result = filterHallucinations(result)
        result = toSimplified(result)
        result = result.trimmingCharacters(in: .whitespacesAndNewlines)
        return result
    }

